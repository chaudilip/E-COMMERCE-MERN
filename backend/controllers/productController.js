// Create New Review or Update the review
exports.createProductReview = catchAsyncErrors(async (req, res, next) => {
  const { rating, comment, productId } = req.body;
  //getting review and comment form productModel
  const review = {
    user: req.user._id,
    name: req.user.name,
    rating: Number(rating), //convert rating into number
    comment,
  };

  const product = await Product.findById(productId);
  // find or foreach method
  const isReviewed = product.reviews.find(
    (rev) => rev.user.toString() === req.user._id.toString() //rev.user is user id  and re.user._id = login id
  );
  // isReviewed means the review has been done before
  if (isReviewed) {
    product.reviews.forEach((rev) => {
      if (rev.user.toString() === req.user._id.toString())
        (rev.rating = rating), (rev.comment = comment); //rating = const review form first review and commet from same also
    });
  } else {
    product.reviews.push(review); //if this is user first review then push the review in review in array
    product.numOfReviews = product.reviews.length; // check the length of reviews
  }
  // check the average of overall reviews
  let avg = 0;
  //check the overall reviews and plus
  product.reviews.forEach((rev) => {
    avg += rev.rating;
  });
  // push the reviews and find the avg = 4, 5 , 5, 2 = 16 / 4 = overall reviews = 4
  product.ratings = avg / product.reviews.length;

  await product.save({ validateBeforeSave: false });

  res.status(200).json({
    success: true,
  });
});

